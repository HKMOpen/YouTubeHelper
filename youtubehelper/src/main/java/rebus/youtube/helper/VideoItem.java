package rebus.youtube.helper;

public class VideoItem {

    private String titleVideo;
    private String descriptionVideo;
    private String likeVideo;
    private String dislikeVideo;
    private String averageVideo;
    private String viewsVideo;
    private String durationVideo;
    private String thumbnailVideo;
    private String urlMediaVideo;
    private String authorVideo;
    private String numberCommentsVideo;
    private String feedCommentsVideo;
    private String categoryVideo;
    private String dateVideo;
    private String thumbnailAuthor;

    public String getTitleVideo() {
        return titleVideo;
    }

    public void setTitleVideo(String titleVideo) {
        this.titleVideo = titleVideo;
    }

    public String getDescriptionVideo() {
        return descriptionVideo;
    }

    public void setDescriptionVideo(String descriptionVideo) {
        this.descriptionVideo = descriptionVideo;
    }

    public String getLikeVideo() {
        return likeVideo;
    }

    public void setLikeVideo(String likeVideo) {
        this.likeVideo = likeVideo;
    }

    public String getDislikeVideo() {
        return dislikeVideo;
    }

    public void setDislikeVideo(String dislikeVideo) {
        this.dislikeVideo = dislikeVideo;
    }

    public String getAverageVideo() {
        return averageVideo;
    }

    public void setAverageVideo(String averageVideo) {
        this.averageVideo = averageVideo;
    }

    public String getViewsVideo() {
        return viewsVideo;
    }

    public void setViewsVideo(String viewsVideo) {
        this.viewsVideo = viewsVideo;
    }

    public String getDurationVideo() {
        return durationVideo;
    }

    public void setDurationVideo(String durationVideo) {
        this.durationVideo = durationVideo;
    }

    public String getThumbnailVideo() {
        return thumbnailVideo;
    }

    public void setThumbnailVideo(String thumbnailVideo) {
        this.thumbnailVideo = thumbnailVideo;
    }

    public String getUrlMediaVideo() {
        return urlMediaVideo;
    }

    public void setUrlMediaVideo(String urlMediaVideo) {
        this.urlMediaVideo = urlMediaVideo;
    }

    public String getAuthorVideo() {
        return authorVideo;
    }

    public void setAuthorVideo(String authorVideo) {
        this.authorVideo = authorVideo;
    }

    public String getNumberCommentsVideo() {
        return numberCommentsVideo;
    }

    public void setNumberCommentsVideo(String numberCommentsVideo) {
        this.numberCommentsVideo = numberCommentsVideo;
    }

    public String getFeedCommentsVideo() {
        return feedCommentsVideo;
    }

    public void setFeedCommentsVideo(String feedCommentsVideo) {
        this.feedCommentsVideo = feedCommentsVideo;
    }

    public String getCategoryVideo() {
        return categoryVideo;
    }

    public void setCategoryVideo(String categoryVideo) {
        this.categoryVideo = categoryVideo;
    }

    public String getDateVideo() {
        return dateVideo;
    }

    public void setDateVideo(String dateVideo) {
        this.dateVideo = dateVideo;
    }

    public String getThumbnailAuthor() {
        return thumbnailAuthor;
    }

    public void setThumbnailAuthor(String thumbnailAuthor) {
        this.thumbnailAuthor = thumbnailAuthor;
    }
}
